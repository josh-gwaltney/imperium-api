import Account from '../models/account';
import Token from '../models/token';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
let postToken = (req, res) => {
  let grantType = req.header('grant_type');
  let username = req.header('username');
  let password = req.header('password');
  res.json({
    grantType: grantType,
    username: username,
    password: password
  });
};

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export { postToken };