class GameFactory {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  constructor(){

  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  create(){
    let cells = [];
    for(let idx = 0; idx < 50; idx++){
      for(let jdx = 0; jdx < 60; jdx++){
        let cell = {
          sprite: 'square',
          x: idx,
          y: jdx,
          h: 1,
          w: 1,
          fill: '#FF0000',
          on: this._chanceOfLife(),

        };
        cells.push(cell);
      }
    }

    return cells;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
  _chanceOfLife(){
    const MIN = 1;
    const MAX = 100;
    const CHANCE = Math.floor(Math.random() * (MAX - MIN)) + MIN;
    if(CHANCE > 75){
      return true;
    }
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default GameFactory;